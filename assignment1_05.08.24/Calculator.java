public class Calculator {
    public int add(int c1, int c2){
        return c1+c2;
    }
    public int add(int c1, int c2, int c3){
        return c1+c2+c3;
    }
    public int add(int c1, int c2, int c3, int c4){
        return c1+c2+c3+c4;
    }
    public int mul(int c1, int c2){
        return c1*c2;
    }
    public int mul(int c1, int c2, int c3){
        return c1*c2*c3;
    }
    public int mul(int c1, int c2, int c3, int c4){
        return c1*c2*c3*c4;
    }
}